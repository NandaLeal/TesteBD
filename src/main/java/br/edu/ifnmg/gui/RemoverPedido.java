    private void btnRemoverPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverPedidoActionPerformed
        Pedido pedido = new Pedido();
        pedido.setCodigo(Long.parseLong(txtCodigo.getText()));
        pedido.setId(pedido.getCodigo());
        
        
        PedidoDao pedidoDao = new PedidoDao();
        
        Pedido pedidoRetornado = new Pedido();
        

        pedidoRetornado = pedidoDao.localizarPorId(pedido.getCodigo());

        if( (pedidoRetornado.getCodigo_vendedor() == 0L 
              || Objects.equals(pedidoRetornado.getCodigo_vendedor(), codVendedor) ) 
         && Objects.equals(pedidoRetornado.getCodigo(), pedido.getCodigo()) ){
            
            pedidoDao.excluir(pedido);
            System.out.println("Pedido REMOVIDO com sucesso.");
            dispose();
        }
        else{
            txtCodigo.requestFocus();
            System.out.println("Codigo do pedido ou do vendedor inv√°lidos!");
        }
        
        

    }//GEN-LAST:event_btnRemoverPedidoActionPerformed

